import turtle

t = turtle.Turtle()
s = turtle.Screen()
s.title("Drawing with a square: Cake")

t.speed(1)
t.shape("classic")
t.write("see the console")
main_cake=input("Enter the color of the main cake: ")
filling=input("Enter the color of the filling: ")
top=input("Enter the color of the top: ")
edge=input("Enter the color of the edge: ")
speed=int(input("Enter the speed: "))
t.speed(speed)
t.shape("square")

def sfs(v):
    for i in range(v):
        t.stamp()
        t.forward(20)
    t.stamp()

#main_cake
t.color(main_cake)
t.pencolor("black")
t.up()
for i in range(6):
    t.goto(-200,20-(20*i))
    sfs(18)
t.goto(-200,-100)
sfs(17)
t.goto(-200,-120)
sfs(15)

#filling
t.color(filling)
t.pencolor("black")
t.goto(0,0)
t.right(180)
sfs(3)
t.goto(0,-20)
sfs(3)
t.goto(-20,20)
sfs(1)
t.goto(-20,-40)
sfs(1)
t.goto(40,0)
t.stamp()
t.goto(120,20)
sfs(1)
for i in range(2):
    t.goto(140,0-(i*20))
    sfs(3)
t.goto(140,-40)
sfs(2)
t.goto(60,-40)
sfs(1)
t.goto(80,-60)
sfs(3)
t.goto(80,-80)
sfs(3)
t.goto(60,-100)
sfs(1)
t.goto(140,-80)
sfs(1)
t.goto(140,-100)
sfs(1)
t.goto(0,-120)
t.stamp()
t.goto(-80,-40)
sfs(1)
t.goto(-80,-60)
sfs(1)
t.goto(-40,-80)
sfs(1)
t.goto(-40,-100)
sfs(1)
t.goto(-100,-100)
t.stamp()
t.goto(-120,20)
t.stamp()
t.goto(-140,0)
sfs(2)
t.goto(-140,-20)
sfs(2)
t.goto(-140,-40)
sfs(2)
t.goto(-140,-80)
sfs(2)
t.goto(-140,-100)
sfs(2)
t.goto(-140,-120)
sfs(1)

#top
t.color(top)
t.pencolor("black")
t.goto(-160,60)
t.right(180)
sfs(16)
t.goto(-120,80)
sfs(12)
t.goto(-60,100)
sfs(8)
t.goto(-20,120)
sfs(4)
    
#edge
t.color(edge)
t.pencolor("black")
t.goto(-200,40)
sfs(18)
t.goto(-180,60)
t.stamp()
t.goto(-160,80)
sfs(1)
t.goto(-120,100)
sfs(2)
t.goto(-60,120)
sfs(1)
t.goto(-20,140)
sfs(6)
t.goto(20,160)
sfs(2)
t.goto(80,120)
sfs(2)
t.goto(120,100)
sfs(2)
t.goto(140,80)
sfs(2)
t.right(90)
sfs(9)
t.right(90)
t.forward(20)
t.stamp()
t.goto(160,-120)
sfs(2)
t.goto(140,-140)
sfs(17)
t.goto(100,-160)
sfs(14)

t.goto(-320,-200)
t.shape("classic")
t.color("black")
t.left(180)
t.write("Image Source: https://depositphotos.com/portfolio-1006486.html?content=vector&sh=9f7ec363c41986605ac4c2fa553716b83fbab2fb&qview=150611488")

